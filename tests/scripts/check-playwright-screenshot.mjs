#!/usr/bin/env node
import { mkdir } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const testsDir = path.resolve(path.dirname(__filename), '..');
const repoRoot = path.resolve(testsDir, '..');
const outputPath = process.env.ROOK_SCREENSHOT_PATH
  ? path.resolve(process.env.ROOK_SCREENSHOT_PATH)
  : path.join(testsDir, 'test-results', 'playwright-smoke.png');

function printHelp(error) {
  console.error('\nPlaywright is installed, but a browser could not be launched.');
  console.error('Reason:', error?.message || error);
  console.error('\nTo fix locally/CI, run one of:');
  console.error('  cd tests && npm run install:browsers');
  console.error('  cd tests && npm run install:browsers:deps');
  console.error('\nIf your environment already has Chrome/Chromium installed, set:');
  console.error('  PLAYWRIGHT_CHROMIUM_EXECUTABLE=/path/to/chromium node tests/scripts/check-playwright-screenshot.mjs');
  console.error('\nIf downloads are blocked with 403, the network/proxy allow-list needs access to Playwright browser downloads or an OS Chromium package mirror.');
}

async function main() {
  const { chromium } = await import('playwright');
  const launchOptions = { headless: true };
  if (process.env.PLAYWRIGHT_CHROMIUM_EXECUTABLE) {
    launchOptions.executablePath = process.env.PLAYWRIGHT_CHROMIUM_EXECUTABLE;
  }

  let browser;
  try {
    browser = await chromium.launch(launchOptions);
  } catch (error) {
    printHelp(error);
    process.exitCode = 1;
    return;
  }

  try {
    await mkdir(path.dirname(outputPath), { recursive: true });
    const page = await browser.newPage({ viewport: { width: 1280, height: 900 } });
    await page.setContent(`
      <!doctype html>
      <html>
        <head>
          <meta charset="utf-8" />
          <title>Rook Playwright Smoke</title>
          <style>
            body { margin: 0; min-height: 100vh; display: grid; place-items: center; background: radial-gradient(circle at top, #1e1b4b, #080b1a 60%); color: #f8fafc; font-family: Arial, sans-serif; }
            .card { border: 1px solid rgba(167,139,250,.45); background: rgba(15,23,42,.82); border-radius: 18px; padding: 28px; text-align: center; box-shadow: 0 20px 80px rgba(0,0,0,.35); }
            img { width: 88px; height: 88px; object-fit: contain; filter: drop-shadow(0 0 16px rgba(167,139,250,.8)); }
            h1 { margin: 14px 0 6px; font-size: 26px; }
            p { margin: 0; color: #cbd5e1; }
          </style>
        </head>
        <body>
          <div class="card">
            <img src="file://${path.join(repoRoot, 'frontend/public/images/logo-mini.png')}" alt="Rook dragon mark" />
            <h1>Playwright screenshots are working</h1>
            <p>Generated by tests/scripts/check-playwright-screenshot.mjs</p>
          </div>
        </body>
      </html>
    `);
    await page.screenshot({ path: outputPath, fullPage: true });
    console.log(`Playwright screenshot created: ${outputPath}`);
  } finally {
    await browser.close();
  }
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});

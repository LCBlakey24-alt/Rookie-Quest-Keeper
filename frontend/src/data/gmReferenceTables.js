function slugify(value) {
  return String(value || 'table').toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '') || 'table';
}

function titleCase(value) {
  return String(value || '').toLowerCase().replace(/\b\w/g, letter => letter.toUpperCase()).replace(/Dcs/g, 'DCs').replace(/Dc/g, 'DC').replace(/Hp/g, 'HP').replace(/Ac/g, 'AC').replace(/Npc/g, 'NPC').replace(/D100/g, 'd100').replace(/D20/g, 'd20').replace(/D12/g, 'd12');
}

function inferCategory(title) {
  const lower = title.toLowerCase();
  if (lower.includes('weapon') || lower.includes('ammunition')) return 'weapons';
  if (lower.includes('armour') || lower.includes('armor')) return 'armour';
  if (lower.includes('potion') || lower.includes('poison') || lower.includes('herb')) return 'potions';
  if (lower.includes('cost') || lower.includes('value') || lower.includes('expense') || lower.includes('service') || lower.includes('food') || lower.includes('mount')) return 'prices';
  if (lower.includes('travel') || lower.includes('watch') || lower.includes('rest')) return 'travel';
  if (lower.includes('quirk') || lower.includes('fate') || lower.includes('opian')) return 'fate';
  if (lower.includes('combat') || lower.includes('action') || lower.includes('reaction') || lower.includes('cover') || lower.includes('condition') || lower.includes('damage') || lower.includes('death')) return 'rules';
  if (lower.includes('trap') || lower.includes('door') || lower.includes('falling') || lower.includes('object') || lower.includes('harvesting')) return 'rules';
  return 'general';
}

function findHeaderIndex(lines) {
  return lines.findIndex(line => line.includes('|'));
}

function diceFromHeader(headerCell) {
  const match = String(headerCell || '').trim().match(/^d(\d+)(?:\s+range)?$/i);
  return match ? `d${match[1]}` : 'reference';
}

function makeText(columns, row) {
  return columns.slice(1).map((column, index) => `${column}: ${row[index + 1] || '—'}`).join(' | ');
}

function parseReferencePack(raw) {
  return raw
    .split(/\n---\n/g)
    .map(section => section.trim())
    .filter(Boolean)
    .map(section => {
      const lines = section.split('\n').map(line => line.trim()).filter(Boolean);
      const headerIndex = findHeaderIndex(lines);
      if (headerIndex <= 0) return null;
      const title = titleCase(lines[0]);
      const description = lines.slice(1, headerIndex).join(' ');
      const columns = lines[headerIndex].split('|').map(part => part.trim()).filter(Boolean);
      const die = diceFromHeader(columns[0]);
      const entries = lines.slice(headerIndex + 1).map((line, index) => {
        const row = line.split('|').map(part => part.trim());
        const cells = Object.fromEntries(columns.map((column, colIndex) => [column, row[colIndex] || '']));
        return {
          range: row[0] || String(index + 1),
          text: makeText(columns, row),
          cells,
        };
      }).filter(entry => entry.range && entry.text);
      const rollable = die !== 'reference' && entries.every(entry => /^\d+(?:\s*[–-]\s*\d+)?$/.test(String(entry.range).trim()));
      return {
        id: `gm-ref-${slugify(title)}`,
        name: title,
        category: inferCategory(title),
        description: description || 'GM reference table.',
        die,
        rollable,
        locked: true,
        source: 'gm-reference-pack',
        columns,
        entries,
      };
    })
    .filter(Boolean);
}


export function formatTiaKartaNpcNotes(seed) {
  const npc = seed.payload;
  const sections = [
    'Tia Karta / Tiamina campaign NPC seed.',
    seed.category ? `Category: ${seed.category}` : '',
    seed.playerSummary ? `Player-facing summary: ${seed.playerSummary}` : '',
    seed.gmSecrets ? `GM-only secrets: ${seed.gmSecrets}` : '',
    seed.importantLocations?.length ? `Important locations: ${seed.importantLocations.join(', ')}` : '',
    seed.hooks?.length ? `Adventure hooks:\n- ${seed.hooks.join('\n- ')}` : '',
    seed.tbd ? `TBD: ${seed.tbd}` : '',
    '',
    'Suggested statistics:',
    `Level ${npc.level} ${npc.race || 'TBD'} ${npc.class_name || 'NPC'} | AC ${npc.ac} | HP ${npc.hp}/${npc.max_hp || npc.hp} | Speed ${npc.speed || '30 ft.'} | PB +${npc.proficiency_bonus || 2}`,
    npc.stats ? `Stats: STR ${npc.stats.strength}, DEX ${npc.stats.dexterity}, CON ${npc.stats.constitution}, INT ${npc.stats.intelligence}, WIS ${npc.stats.wisdom}, CHA ${npc.stats.charisma}` : '',
    npc.saving_throws?.length ? `Saving throws: ${npc.saving_throws.join(', ')}` : '',
    npc.skills?.length ? `Skills: ${npc.skills.join(', ')}` : '',
    npc.attacks?.length ? `Attacks:\n- ${npc.attacks.map(item => `${item.name} ${item.bonus ? `(${item.bonus})` : ''}: ${item.damage}${item.notes ? ` — ${item.notes}` : ''}`).join('\n- ')}` : '',
    npc.abilities?.length ? `Abilities:\n- ${npc.abilities.map(item => `${item.name}: ${item.description}`).join('\n- ')}` : '',
    npc.spells ? `Spells: ${npc.spells.casting_ability || 'TBD'} caster, save DC ${npc.spells.spell_save_dc || 0}, attack ${npc.spells.spell_attack_bonus || 0}. Cantrips: ${(npc.spells.cantrips || []).join(', ') || 'none'}. Known: ${(npc.spells.known_spells || []).join(', ') || 'none'}.` : '',
  ];

  return sections.filter(Boolean).join('\n');
}

export function getTiaKartaNpcPayload(seed) {
  return {
    ...seed.payload,
    notes: [formatTiaKartaNpcNotes(seed), seed.payload.notes].filter(Boolean).join('\n\n'),
  };
}


const spell = (name, level, school = '', description = '') => ({
  name,
  level,
  school,
  description,
});

const item = (name, overrides = {}) => ({
  name,
  type: 'Item',
  quantity: 1,
  description: '',
  ...overrides,
});

const weapon = (name, damageDice, damageType, overrides = {}) => item(name, {
  type: 'Weapon',
  damage_dice: damageDice,
  damage_type: damageType,
  equip_slot: 'mainHand',
  equipped_slot: 'mainHand',
  equipped: true,
  is_equipped: true,
  ...overrides,
});

